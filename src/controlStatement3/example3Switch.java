package controlStatement3;
//Switch programming
public class example3Switch {

	public static void main(String[] args) {
		String student = "jaya";
		switch (student)
		{
		case "Shilpa":
		    System.out.println("She is a bright student");
		break;
		case " Disha":
			System.out.println("She is a very good Girl");
		break;
		case "Shweta":
			System.out.println("She is a Simple Girl");
		break;
		case "Sharda":
			System.out.println("She is a honest girl");
		break;
		case "Durga":
			System.out.println("She is a honest and very good student");
		break;
		
		default:
			System.out.println("Sakshi is a very beutiful girl");
			}

	}

}
